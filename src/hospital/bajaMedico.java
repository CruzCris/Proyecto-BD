    private void eliminarMedico(String email) {
       int idMed = 0;
       conexion conectar = new conexion();
       Statement st = conectar.Conectar();
       try{
           ResultSet rs = st.executeQuery("SELECT COUNT(*) AS existe FROM Medico WHERE email='"+email+"'");
           if(rs.next()){
               int validacion = rs.getInt("existe");
               if(validacion > 0){
                   rs = st.executeQuery("SELECT idMed FROM Medico WHERE email='"+email+"'");
                    while(rs.next()){
                        idMed = rs.getInt("idMed");
                    }
                    rs = st.executeQuery("SELECT COUNT(*) AS existe FROM Cita WHERE idMed="+idMed+" AND estadoCita = 1");
                    if(rs.next()){
                        validacion = rs.getInt("existe");
                        if(validacion == 0){
                            String eliminarMedico = "DELETE FROM Usuario WHERE email='"+email+"'";
                            int filasEliminadas = st.executeUpdate(eliminarMedico);
                            if(filasEliminadas > 0){
                                JOptionPane.showMessageDialog(null, "Se dio de baja al médico correctamente");
                            }else{
                                JOptionPane.showMessageDialog(null, "No se pudo dar de baja al médico");
                            }
                        }else{
                            JOptionPane.showMessageDialog(null, "No se puede dar de baja al médico, debido a que este aún tiene citas por atender");
                        }
                    }
               }else{
                   JOptionPane.showMessageDialog(null, "No existe ningún médico con dicho email asociado");
                   txtEmail.setText("");
                   txtEmail2.setText("");
               }
           }
       }catch(SQLException e){
           JOptionPane.showMessageDialog(null, e);
       }
    }
